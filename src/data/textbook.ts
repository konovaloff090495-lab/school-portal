// ─── Публичный список тем ──────────────────────────────────────────────────────

export interface TextbookTopicMeta {
  slug: string
  title: string
  subject: string
  klass: number
  excerpt: string
}

export const textbookTopics: TextbookTopicMeta[] = Object.entries(RAW_TOPICS).flatMap(
  ([subject, byClass]) =>
    Object.entries(byClass).flatMap(([klass, topics]) =>
      topics.map(t => ({ ...t, subject, klass: Number(klass) }))
    )
)

// ─── Хелперы ──────────────────────────────────────────────────────────────────

export function getSubjectBySlug(slug: string): TextbookSubject | undefined {
  return textbookSubjects.find(s => s.slug === slug)
}

export function getTopicsForSubjectAndClass(subject: string, klass: number): TextbookTopicMeta[] {
  return textbookTopics.filter(t => t.subject === subject && t.klass === klass)
}

export function getTopicBySlug(subject: string, klass: number, slug: string): TextbookTopicMeta | undefined {
  return textbookTopics.find(t => t.subject === subject && t.klass === klass && t.slug === slug)
}

export function klassLabel(klass: number): string {
  return `${klass} класс`
}

export function klassLabelIn(klass: number): string {
  return `${klass} классе`
}

export const subjectSlugs = textbookSubjects.map(s => s.slug)

