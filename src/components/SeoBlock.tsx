// Предложный падеж: "обучение в ..."
const typeInForm: Record<SchoolType, string> = {
  gosudarstvennye: 'государственных школах',
  chastnie:        'частных школах',
  online:          'онлайн-школах',
  vechernie:       'вечерних школах',
  eksternal:       'школах-экстернатах',
  semejnye:        'семейных школах',
  domashnie:       'школах с надомным обучением',
  'pri-vuzakh':    'школах при вузах',
  profilnye:       'профильных школах',
  gimnazii:        'гимназиях',
  korrektsionnye:  'коррекционных школах',
  kadetskie:       'кадетских школах',
  mezhdunarodnie:  'международных школах',
}

function typedCount(n: number, t: SchoolType): string {
  const [one, few, many] = typeCountForms[t]
  const mod10 = n % 10, mod100 = n % 100
  if (mod10 === 1 && mod100 !== 11) return `${n} ${one}`
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 10 || mod100 >= 20)) return `${n} ${few}`
  return `${n} ${many}`
}

// ── Основной компонент ────────────────────────────────────────────────────────
export default function SeoBlock({ region, type, feature, count = 0, metro, district, city }: SeoBlockProps) {
  const regionName  = region ? regionLabels[region]  : null
  const regionIn    = region ? regionLabelsIn[region] : null
  const regionOf    = region ? regionLabelsOf[region] : null
  const context     = region ? (regionContext[region] ?? null) : null
  const info        = type ? typeInfo[type] : null
  const exam        = feature ? (examInfo[feature] ?? null) : null

  const locationLabel = metro
    ? `у метро «${metro}»`
    : district
    ? `в ${district}`
    : city
    ? `в ${city}`
    : regionIn ?? 'в России'

  // Счётная форма: с типом ("10 онлайн-школ") или без ("10 школ")
  const countStr = count > 0
    ? (type ? typedCount(count, type) : `${count} ${count === 1 ? 'школа' : count >= 2 && count <= 4 ? 'школы' : 'школ'}`)
    : (type ? `несколько ${typeCountForms[type][2]}` : 'несколько школ')

  const sections: { h: string; p: string }[] = []

  // ── 1. Вводный раздел ─────────────────────────────────────────────────────
  if (exam && region) {
    // Страница подготовки к экзамену в городе
    sections.push({
      h: `${exam.title} ${locationLabel}: что важно знать`,
      p: `В каталоге ${locationLabel} — ${countStr} с программами подготовки. ${exam.formats}`,
    })
  } else if (exam && !region) {
    // Глобальная страница подготовки к экзамену
    sections.push({
      h: `${exam.title} в России: обзор`,
      p: `В нашем каталоге представлено ${countStr} с программами подготовки по всей России. ${exam.formats}`,
    })
  } else if (info && region) {
    // Страница типа школы в городе — только тематический контент, без дублирующего regionContext
    const typeLabel = typeInfo[type!].label
    sections.push({
      h: `${typeLabel.charAt(0).toUpperCase() + typeLabel.slice(1)} ${locationLabel}: обзор`,
      p: `${locationLabel.charAt(0).toUpperCase() + locationLabel.slice(1)} представлено ${countStr} этого типа. Такие учреждения подходят для ${info.who}.`,
    })
  } else if (info && !region) {
    sections.push({
      h: `${countStr} в России`,
      p: `В каталоге собраны ${typeInfo[type!].label.toLowerCase()} по всей России. Используйте фильтр по городу, чтобы найти школу в своём регионе. Этот тип учреждений подходит для ${info.who}.`,
    })
  } else if (region && !type && !exam) {
    // Главная страница города — здесь regionContext уместен
    sections.push({
      h: `Как выбрать школу ${regionIn}`,
      p: `${context ? context + ' ' : ''}В каталоге — ${countStr} всех форм обучения: государственные, частные, онлайн-школы, гимназии, лицеи и экстернат. Используйте фильтры по типу, рейтингу и стоимости, чтобы найти подходящий вариант.`,
    })
  } else if (metro) {
    sections.push({
      h: `Школы у метро «${metro}»: как выбрать`,
      p: `У станции метро «${metro}» представлено ${countStr}. При выборе учитывайте тип (государственная, гимназия или частная), наличие профильных программ, рейтинг ЕГЭ и время в пути от дома до школы.`,
    })
  }

  // ── 2–5. Секции типа школы (только если нет exam-режима) ────────────────
  if (info && !exam) {
    const inForm = type ? typeInForm[type] : info.label
    sections.push({
      h: `Что даёт обучение в ${inForm}`,
      p: info.pros.map((p, i) => `${i + 1}. ${p}`).join('. ') + '.',
    })
    sections.push({
      h: `Как поступить`,
      p: info.how,
    })
    sections.push({
      h: `Стоимость обучения`,
      p: info.price,
    })
    sections.push({
      h: `Совет при выборе`,
      p: info.tip,
    })
  }

  // ── 6. ЕГЭ/ОГЭ блоки ────────────────────────────────────────────────────────
  if (exam) {
    sections.push({
      h: `Для кого подходит`,
      p: `Эти школы ориентированы на ${exam.who}. ${exam.subjects}`,
    })
    if (type === 'online') {
      sections.push({
        h: `Онлайн-подготовка`,
        p: exam.online,
      })
    }
    sections.push({
      h: `Совет при выборе`,
      p: exam.tip,
    })
  }

  // ── 7. Региональные типы (только для страниц города без типа) ────────────
  if (region && !type && !metro && !exam) {
    sections.push({
      h: `Типы школ ${regionIn}`,
      p: `Государственные школы — бесплатно, по прописке. Гимназии и лицеи — конкурсный отбор, усиленные программы. Частные школы — малые классы, индивидуальный подход, платно. Онлайн-школы — гибкий график, подходят для спортсменов и путешественников. Экстернат — ускоренное прохождение программы. Семейные школы — альтернативные методики в малых группах. Школы при вузах — профильная подготовка и высокая поступаемость.`,
    })
  }

  if (sections.length === 0) return null

  return (
    <div style={{
      maxWidth: 860,
      margin: '0 auto',
      padding: '8px 0 48px',
      color: '#374151',
      fontFamily: 'var(--font-manrope, system-ui)',
    }}>
      {sections.map((s, i) => (
        <div key={i} style={{ marginBottom: 24 }}>
          <h2 style={{
            fontSize: 18,
            fontWeight: 700,
            color: '#1A1814',
            margin: '0 0 8px',
            lineHeight: 1.3,
          }}>
            {s.h}
          </h2>
          <p style={{
            fontSize: 15,
            lineHeight: 1.75,
            margin: 0,
            color: '#4B5563',
          }}>
            {s.p}
          </p>
        </div>
      ))}
    </div>
  )
}
